package io.data.util

/**
 * The 2-letter ISO-639-1 code of the language you want to get headlines for.
 *
 * `Possible options: ar, de, en, es, fr, he, it, nl, no, pt, ru, sv, ud, zh.`
 * */
enum class NewsLanguageType {
    AR,
    DE,
    EN,
    ES,
    FR,
    HE,
    IT,
    NL,
    NO,
    PT,
    RU,
    SV,
    UD,
    ZH
}